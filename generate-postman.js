// Fill in missing configuration with defaults
function completeConfig(userConfig) {
  const defaultConfig = getDefaultConfig();
  const mergedConfig = { ...defaultConfig, ...userConfig };
  
  // Add default collection description if not provided
  if (!mergedConfig.collectionDescription) {
    mergedConfig.collectionDescription = `API collection for ${mergedConfig.collectionName}`;
  }

  // Set up default headers if not provided
  if (!mergedConfig.defaultHeaders) {
    mergedConfig.defaultHeaders = [
      {
        key: 'Authorization',
        value: `Bearer {{${mergedConfig.authTokenName}}}`
      },
      {
        key: 'Content-Type',
        value: 'application/json'
      }
    ];
  }

  // Set up default environment variables if not provided
  if (!mergedConfig.environmentVariables) {
    mergedConfig.environmentVariables = [
      {
        key: 'baseUrl',
        value: mergedConfig.baseUrl,
        type: 'string',
        description: 'Base URL for the API'
      },
      {
        key: mergedConfig.authTokenName,
        value: '',
        type: 'string',
        description: 'Authentication token'
      }
    ];
  }
  
  return mergedConfig;
}

// Load configuration from .docdoc file
function loadConfig() {
  try {
    const configPath = path.join(__dirname, '.docdoc');
    if (fs.existsSync(configPath)) {
      const configData = fs.readFileSync(configPath, 'utf8');
      const userConfig = JSON.parse(configData);
      return completeConfig(userConfig);
    } else {
      console.log('No .docdoc file found. Using default configuration.');
      return getDefaultConfig();
    }
  } catch (error) {
    console.error('Error loading .docdoc configuration:', error);
    console.log('Using default configuration instead.');
    return getDefaultConfig();
  }
}

