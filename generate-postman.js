// Extract endpoint details from route info
function extractEndpointDetails(route) {
  const { path, method, resource, fullPath } = route;
  
  // Generate a more useful description using the dynamic generator
  const description = generateEndpointDescription({ path, method, resource });
  
  // Use the provided fullPath if available, otherwise construct it
  const endpointFullPath = fullPath || (API_PREFIX + path);
  
  return {
    path,
    method,
    resource,
    description,
    fullPath: endpointFullPath
  };
}

// Organize endpoints into folders by resource
function organizeEndpoints(routes) {
  const folders = {};
  
  // Map routes to Postman items
  const items = mapRoutesToPostmanItems(routes);
  
  // Group items by resource
  items.forEach(item => {
    const endpoint = extractEndpointDetails(routes.find(
      route => route.path === item.request.url.path.replace(API_PREFIX, '') && 
               route.method === item.request.method
    ));
    
    const resourceName = endpoint.resource;
    
    if (!folders[resourceName]) {
      folders[resourceName] = {
        name: capitalizeFirstLetter(resourceName),
        item: []
      };
    }
    
    folders[resourceName].item.push(item);
  });
  
  // Sort folders by name
  return Object.values(folders).sort((a, b) => a.name.localeCompare(b.name));
}

// Extract Zod schemas from validator file content
function extractZodSchemas(content, resourceName) {
  const schemas = {};
  
  try {
    // Find Zod schema definitions with improved regex
    const schemaRegex = /const\s+([a-zA-Z0-9]+Schema)\s*=\s*z\.(object|string|number|boolean|array|date)\(\)([^]*?)(?=;\s*($|\/\/|\/\*|export|const|let|var))/gs;
    
    let match;
    while ((match = schemaRegex.exec(content)) !== null) {
      const schemaName = match[1];
      const schemaType = match[2]; // object, string, number, etc.
      const schemaContent = match[3];
      
      // Parse properties for object type schemas
      const properties = {};
      
      if (schemaType === 'object') {
        // Look for shape pattern
        const shapeRegex = /\.shape\(\s*{([^]*?)}\s*\)/s;
        const shapeMatch = shapeRegex.exec(schemaContent);
        
        if (shapeMatch) {
          // Get field definitions
          const fieldsContent = shapeMatch[1];
          
          // Match field patterns, handling multi-line definitions
          const fieldPattern = /\s*([a-zA-Z0-9_]+)\s*:\s*z\.([^,]*?),?(?=\s*\w+\s*:|$)/gs;
          let fieldMatch;
          
          while ((fieldMatch = fieldPattern.exec(fieldsContent)) !== null) {
            try {
              const fieldName = fieldMatch[1];
              const fieldDef = fieldMatch[2];
              
              // Skip empty matches
              if (!fieldName || !fieldDef) continue;
              
              // Parse field type and constraints
              parseZodField(properties, fieldName, fieldDef);
            } catch (fieldError) {
              console.warn(`Error parsing field in schema ${schemaName}: ${fieldError.message}`);
            }
          }
        }
      }
      
      schemas[`${resourceName}.${schemaName}`] = {
        name: schemaName,
        resource: resourceName,
        properties
      };
    }
  } catch (error) {
    console.error(`Error extracting Zod schemas from ${resourceName}: ${error.message}`);
  }
  
  return schemas;
}

// Parse a single Zod field definition
function parseZodField(properties, fieldName, fieldDef) {
  // Initialize defaults
  let fieldType = 'string';
  let isRequired = true;
  let isArray = false;
  let isObject = false;
  let min, max, defaultValue, enumValues;
  
  // Detect array types
  if (fieldDef.includes('array()')) {
    isArray = true;
    fieldType = 'array';
    
    // Determine array item type
    const ofTypeMatch = /\.of\(z\.(\w+)/.exec(fieldDef);
    if (ofTypeMatch) {
      let itemType = ofTypeMatch[1];
      
      // Handle array of objects with nested properties
      if (itemType === 'object' && fieldDef.includes('shape(')) {
        itemType = 'object';
        // Future enhancement: parse nested object fields
      }
      
      fieldType = itemType;
    }
  }
  // Detect object types with shape
  else if (fieldDef.includes('object()') && fieldDef.includes('shape(')) {
    isObject = true;
    fieldType = 'object';
    // Future enhancement: parse nested object fields
  }
  // Handle string type with validations
  else if (fieldDef.startsWith('string')) {
    fieldType = 'string';
    
    // Extract common string validations
    if (fieldDef.includes('.email()')) fieldType = 'email';
    if (fieldDef.includes('.url()')) fieldType = 'url';
    if (fieldDef.includes('.uuid()')) fieldType = 'uuid';
    
    // Get string constraints
    min = extractNumber(fieldDef, /\.min\((\d+)\)/);
    max = extractNumber(fieldDef, /\.max\((\d+)\)/);
    defaultValue = extractDefault(fieldDef, /\.default\(['"]([^'"]*)['"]\)/);
  }
  // Handle number type with validations
  else if (fieldDef.startsWith('number')) {
    fieldType = 'number';
    
    // Extract number validations
    if (fieldDef.includes('.int()')) fieldType = 'integer';
    
    // Get number constraints
    min = extractNumber(fieldDef, /\.min\((\d+(?:\.\d+)?)\)/);
    max = extractNumber(fieldDef, /\.max\((\d+(?:\.\d+)?)\)/);
    defaultValue = extractNumber(fieldDef, /\.default\((\d+(?:\.\d+)?)\)/);
  }
  // Handle boolean type
  else if (fieldDef.startsWith('boolean')) {
    fieldType = 'boolean';
    defaultValue = fieldDef.includes('.default(true)') ? true : 
                  fieldDef.includes('.default(false)') ? false : undefined;
  }
  // Handle date type
  else if (fieldDef.startsWith('date')) {
    fieldType = 'date';
  }
  // Handle enum types
  else if (fieldDef.includes('enum(') || fieldDef.includes('nativeEnum(')) {
    fieldType = 'enum';
    // Try to extract enum values from different formats
    try {
      // Extract from array format [...]
      const enumMatch = /\[(.*?)\]/.exec(fieldDef);
      if (enumMatch) {
        enumValues = parseZodEnumValues(enumMatch[1]);
      }
    } catch (e) {
      console.warn(`Error parsing enum values: ${e.message}`);
    }
  }
  
  // Check for optional and nullable modifiers
  isRequired = !fieldDef.includes('.optional()');
  const isNullable = fieldDef.includes('.nullable()');
  
  // Store parsed field data
  properties[fieldName] = {
    type: fieldType,
    required: isRequired,
    isNullable,
    min,
    max,
    default: defaultValue,
    enum: enumValues,
    isArray,
    isObject
  };
}

// Helper to extract numeric values from patterns
function extractNumber(str, pattern) {
  const match = pattern.exec(str);
  return match ? parseFloat(match[1]) : undefined;
}

// Helper to extract default values
function extractDefault(str, pattern) {
  const match = pattern.exec(str);
  return match ? match[1] : undefined;
}

// Helper function to parse Zod enum values
function parseZodEnumValues(enumStr) {
  try {
    return enumStr.split(',').map(val => {
      val = val.trim();
      // Remove quotes if present
      if ((val.startsWith('\'') && val.endsWith('\'')) || 
          (val.startsWith('"') && val.endsWith('"'))) {
        return val.substring(1, val.length - 1);
      }
      // Convert to number if numeric
      if (!isNaN(Number(val))) return Number(val);
      return val;
    });
  } catch (e) {
    return [];
  }
} 