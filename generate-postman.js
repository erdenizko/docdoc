const validationSchemas = {};

// Load validation schemas from validator files
function loadValidationSchemas() {
  try {
    if (!fs.existsSync(VALIDATORS_DIR)) {
      console.log(`Validators directory ${VALIDATORS_DIR} does not exist. Skipping validator loading.`);
      return;
    }

    const validatorFiles = fs.readdirSync(VALIDATORS_DIR)
      .filter(file => file.endsWith(VALIDATOR_FILE_PATTERN));
    
    let schemasCount = 0;
    
    validatorFiles.forEach(file => {
      try {
        const resourceName = file.replace(VALIDATOR_FILE_PATTERN, '');
        const filePath = path.join(VALIDATORS_DIR, file);
        const content = fs.readFileSync(filePath, 'utf8');
        
        // Extract schemas from content based on config
        let schemas = {};
        if (config.zod) {
          // Check if content uses Zod
          if (content.includes('const z = require(\'zod\')') || content.includes('import { z } from \'zod\'') || content.includes('import z from \'zod\'')) {
            schemas = extractZodSchemas(content, resourceName);
          } else {
            // Fallback to regular schema extraction if Zod import not found
            schemas = extractSchemas(content, resourceName);
          }
        } else {
          schemas = extractSchemas(content, resourceName);
        }
        
        Object.assign(validationSchemas, schemas);
        schemasCount += Object.keys(schemas).length;
      } catch (error) {
        console.error(`Error processing validator file ${file}:`, error);
      }
    });
    
    console.log(`Loaded ${schemasCount} validation schemas`);
  } catch (error) {
    console.error('Error loading validation schemas:', error);
  }
}

// Extract schemas from validator file content
function extractSchemas(content, resourceName) {
  const schemas = {};
  
  // Find schema definitions - match the entire schema including nested objects
  const schemaRegex = /const\s+([a-zA-Z0-9]+Schema)\s*=\s*{([^]*?)(?=\n};)/gs;
  const propertyRegex = /\s+([a-zA-Z0-9_]+):\s*{([^}]+)}/gs;
  const typeRegex = /type\s*:\s*['"]([^'"]+)['"]/i;
  const requiredRegex = /required\s*:\s*(true|false)/i;
  const minRegex = /min\s*:\s*(\d+)/i;
  const maxRegex = /max\s*:\s*(\d+)/i;
  const defaultRegex = /default\s*:\s*([^,\s]+)/i;
  const enumRegex = /enum\s*:\s*\[(.*?)\]/i;
  const itemsRegex = /items\s*:\s*{([^}]+)}/i;
  const propsRegex = /props\s*:\s*{([^}]+)}/i;
  
  let match;
  while ((match = schemaRegex.exec(content)) !== null) {
    const schemaName = match[1];
    const schemaContent = match[2];
    
    // Parse properties
    const properties = {};
    let propertyMatch;
    
    // Reset propertyRegex lastIndex to ensure we start from the beginning
    propertyRegex.lastIndex = 0;
    
    while ((propertyMatch = propertyRegex.exec(schemaContent)) !== null) {
      const propertyName = propertyMatch[1];
      const propertyContent = propertyMatch[2];
      
      // Basic property type extraction
      const typeMatch = typeRegex.exec(propertyContent);
      const requiredMatch = requiredRegex.exec(propertyContent);
      const minMatch = minRegex.exec(propertyContent);
      const maxMatch = maxRegex.exec(propertyContent);
      const defaultMatch = defaultRegex.exec(propertyContent);
      const enumMatch = enumRegex.exec(propertyContent);
      const itemsMatch = itemsRegex.exec(propertyContent);
      const propsMatch = propsRegex.exec(propertyContent);
      
      properties[propertyName] = {
        type: typeMatch ? typeMatch[1] : 'string',
        required: requiredMatch ? requiredMatch[1] === 'true' : false,
        min: minMatch ? parseInt(minMatch[1]) : undefined,
        max: maxMatch ? parseInt(maxMatch[1]) : undefined,
        default: defaultMatch ? parseDefaultValue(defaultMatch[1]) : undefined,
        enum: enumMatch ? parseEnumValues(enumMatch[1]) : undefined,
        isArray: propertyContent.includes('type: \'array\''),
        isObject: propertyContent.includes('type: \'object\''),
        hasItems: itemsMatch !== null,
        hasProps: propsMatch !== null
      };
      
      // Handle nested arrays with item definitions
      if (itemsMatch) {
        const itemTypeMatch = typeRegex.exec(itemsMatch[1]);
        properties[propertyName].itemType = itemTypeMatch ? itemTypeMatch[1] : 'string';
      }
      
      // Check if property is a nested object
      if (propertyContent.includes('props:') || propertyContent.includes('items:')) {
        properties[propertyName].isComplex = true;
      }
    }
    
    // Look for nested objects and arrays with complex item types
    const nestedObjectRegex = /\s+([a-zA-Z0-9_]+):\s*{[^}]*props\s*:\s*{([^]*?)(?=\s+}\s+})/gs;
    const nestedArrayRegex = /\s+([a-zA-Z0-9_]+):\s*{[^}]*items\s*:\s*{[^}]*type\s*:\s*['"]object['"]/gs;
    
    let nestedMatch;
    
    // Process nested objects
    while ((nestedMatch = nestedObjectRegex.exec(schemaContent)) !== null) {
      const propName = nestedMatch[1];
      if (properties[propName]) {
        properties[propName].isNestedObject = true;
        
        // Extract nested properties
        const nestedPropsRegex = /props\s*:\s*{([^]*?)(?=\s+}\s+})/s;
        const nestedPropsMatch = nestedPropsRegex.exec(nestedMatch[0]);
        
        if (nestedPropsMatch) {
          properties[propName].nestedProps = {};
          const nestedPropertyRegex = /\s+([a-zA-Z0-9_]+):\s*{([^}]+)}/gs;
          let nestedPropMatch;
          
          while ((nestedPropMatch = nestedPropertyRegex.exec(nestedPropsMatch[1])) !== null) {
            const nestedPropName = nestedPropMatch[1];
            const nestedPropContent = nestedPropMatch[2];
            
            // Extract nested property details
            const nestedTypeMatch = typeRegex.exec(nestedPropContent);
            const nestedRequiredMatch = requiredRegex.exec(nestedPropContent);
            
            properties[propName].nestedProps[nestedPropName] = {
              type: nestedTypeMatch ? nestedTypeMatch[1] : 'string',
              required: nestedRequiredMatch ? nestedRequiredMatch[1] === 'true' : false
            };
          }
        }
      }
    }
    
    // Process arrays with object items
    while ((nestedMatch = nestedArrayRegex.exec(schemaContent)) !== null) {
      const propName = nestedMatch[1];
      if (properties[propName]) {
        properties[propName].hasComplexItems = true;
        
        // Extract item properties
        const itemPropsRegex = /props\s*:\s*{([^]*?)(?=\s+}\s+})/s;
        const itemPropsMatch = itemPropsRegex.exec(nestedMatch[0]);
        
        if (itemPropsMatch) {
          properties[propName].itemProps = {};
          const itemPropertyRegex = /\s+([a-zA-Z0-9_]+):\s*{([^}]+)}/gs;
          let itemPropMatch;
          
          while ((itemPropMatch = itemPropertyRegex.exec(itemPropsMatch[1])) !== null) {
            const itemPropName = itemPropMatch[1];
            const itemPropContent = itemPropMatch[2];
            
            // Extract item property details
            const itemTypeMatch = typeRegex.exec(itemPropContent);
            const itemRequiredMatch = requiredRegex.exec(itemPropContent);
            
            properties[propName].itemProps[itemPropName] = {
              type: itemTypeMatch ? itemTypeMatch[1] : 'string',
              required: itemRequiredMatch ? itemRequiredMatch[1] === 'true' : false
            };
          }
        }
      }
    }
    
    schemas[`${resourceName}.${schemaName}`] = {
      name: schemaName,
      resource: resourceName,
      properties
    };
  }
  
  return schemas;
}

// Helper function to parse default values
function parseDefaultValue(defaultStr) {
  if (defaultStr === 'true') return true;
  if (defaultStr === 'false') return false;
  if (!isNaN(Number(defaultStr))) return Number(defaultStr);
  return defaultStr;
}

// Helper function to parse enum values
function parseEnumValues(enumStr) {
  try {
    return enumStr.split(',').map(val => {
      val = val.trim();
      // Remove quotes if present
      if ((val.startsWith('\'') && val.endsWith('\'')) || 
          (val.startsWith('"') && val.endsWith('"'))) {
        return val.substring(1, val.length - 1);
      }
      // Convert to number if numeric
      if (!isNaN(Number(val))) return Number(val);
      return val;
    });
  } catch (e) {
    return [];
  }
}

// Get endpoint name for Postman
function getEndpointName(endpoint) {
  const { method, path, resource } = endpoint;
  
  const action = {
    GET: 'Get',
    POST: 'Create',
    PUT: 'Update',
    DELETE: 'Delete',
    PATCH: 'Modify'
  }[method] || method;
  
  const resourceDisplay = endpoint.resource.charAt(0).toUpperCase() + endpoint.resource.slice(1);
  
  if (path === '/' || path === '') {
    // Listing endpoint
    return `${action} All ${pluralize(resourceDisplay)}`;
  } else if (path.includes(':')) {
    // Find the parameter in the path
    const paramMatch = path.match(/:([a-zA-Z0-9_]+)/);
    const param = paramMatch ? paramMatch[1] : 'id';
    
    // Single resource endpoint with parameter
    return `${action} ${resourceDisplay} By ${param.charAt(0).toUpperCase() + param.slice(1)}`;
  } else if (path.includes('/')) {
    // Path contains a sub-resource or action
    const parts = path.split('/').filter(p => p);
    if (parts.length > 0) {
      const lastPart = parts[parts.length - 1];
      // Convert kebab-case or snake_case to Title Case
      const formattedAction = lastPart
        .replace(/[-_]/g, ' ')
        .replace(/\w\S*/g, w => w.charAt(0).toUpperCase() + w.substr(1).toLowerCase());
      
      return `${formattedAction} ${resourceDisplay}`;
    }
  }
  
  // Default
  return `${action} ${resourceDisplay}`;
}

